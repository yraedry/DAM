package org.dam.programacion.basics;

public class ImpresionVariables {
    public static void main(String[] args) {
        int i = 10;
        System.out.println("El valor de i es de "+ i);
        System.out.println("El valor de i++ es de "+ i++);
        System.out.println("El valor de ++i es de "+ ++i);
    }
}
